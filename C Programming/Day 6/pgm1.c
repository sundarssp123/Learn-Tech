#include<stdio.h>
#include<stdlib.h>
#include<math.h>

int main()
{
    printf("The Result of Absolute Function is %d\n",abs(-49));
    printf("The Result of SquareRoot Function is %f\n",sqrt(25));
    printf("The Result of LogBase10 Function is %f\n",log10(10));
    printf("The Result of Sin Function is %f\n",sin(90));
    printf("The Result of Power Function is %f\n",pow(2,3));
    printf("The Result of Ceil Function is %f\n",ceil(7.8));
    printf("The Result of Exp Function is %f\n",exp(2));
    printf("The Result of Tan Function is %f\n",tan(90));
    return 0;    
}
